function Keit2400SourceChoose( src, ~ ,data_object,gui_object)
    % changes interface according to source choice
    
    % get structs from objects
    data=guidata(data_object);
    gui=guidata(gui_object);
    
    v=get(src,'value');
    set(gui.MeasureRangePopup,'Value',1);
    set(gui.SourceRangePopup,'Value',1);
    if v==1
        set(gui.SourceRangePopup,'string',{'210 V',...
            '21 V',...
            '2.1 V',...
            '210 mV'});
    set(gui.MeasureCompUnitPopup,'string',{'A',...
            'mA','에'});
    set(gui.MeasureRangePopup,'string',{'1.05 A',...
            '105 mA',...
            '10.5 mA',...
            '1.05 mA',...
            '105 에',...
            '10.5 에',...
            '1.05 에'});
    elseif v==2
        set(gui.SourceRangePopup,'string',{'1.05 A',...
            '105 mA',...
            '10.5 mA',...
            '1.05 mA',...
            '105 에',...
            '10.5 에',...
            '1.05 에'});
        set(gui.MeasureCompUnitPopup,'string',{'V',...
            'mV','엤'});
        set(gui.MeasureRangePopup,'string',{'210 V',...
            '21 V',...
            '2.1 V',...
            '210 mV'});
    end
end